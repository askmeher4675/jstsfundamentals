//push() -  adding elements at end of an array
let arr1 = [1, 2];
arr1.push(3);
console.log("Push:", arr1); // [1, 2, 3]

//pop() - removes last element form the array
arr1.pop();
console.log("Pop:", arr1); // [1, 2]

//shift() - removes first element from left side
arr1.shift();
console.log("Shift:", arr1); //[2]

//unshift() - adds element to the initial array
arr1.unshift(4);
console.log("Shift:", arr1); //[4,2]

